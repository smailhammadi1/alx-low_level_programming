#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: Generate a random number, determine its last digit,
 *              and print a message based on the last digit.
 *
 * Return: Always 0 (Success)
 */

int main(void)
{
int i;
for (i = 0; i <= 9; i++)
{
printf("%d", i);
}
printf("\n");
return (0);
}
